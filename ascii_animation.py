class Animation:
    def __init__(self, screen, frames):
        self.screen = screen
        self.frames = frames
        self.FRAME_DELAY = 0.2  # seconds
        self.END_DELAY = 3  # seconds

    def play(self):
        for frame in self.frames:
            self.screen.clear()
            self.screen.addstr(frame)
            self.screen.refresh()
            time.sleep(self.FRAME_DELAY)
        self.screen.clear()
        time.sleep(self.END_DELAY)


def main(screen, frames):
    # Boilerplate code to initialize our colors.
    # To use them later, just do:
    #
    #   self.screen.addstr(' - ', curses.color_pair(0))   # or pair #1, 2, 3, whichever you prefer
    #
    curses.start_color()
    curses.use_default_colors()
    for i in range(0, curses.COLORS):
        curses.init_pair(i + 1, i, -1)

    # now we create and begin our animation!
    animation = Animation(screen, frames)
    animation.play()

# This wrapper() method from the curses library initializes our screen.
# See https://docs.python.org/3/library/curses.html#curses.wrapper
wrapper(main, frames)  
